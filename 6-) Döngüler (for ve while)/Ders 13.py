"""Döngüler bizim belirli bir kod
bloğunun tekrar tekrar çalışmasını
sağlayan yapılardır."""
# For döngüsü belirli bir elemanları gezinmek için
# kullanılır.
# Örnek: Listeler de for döngüsü
şehirler = ["İstanbul", "Ankara", 
"İzmir", "Bursa", "Adana"]
for şehir in şehirler:
    print(şehir)
#  Bir koşulu sağladığı sürece döngüyü çalıştırmak
#  için kullanılır.
# Örnek: Sayılar da while döngüsü 
sayı = 1
while sayı <= 5:
    print(sayı)
    sayı += 1
