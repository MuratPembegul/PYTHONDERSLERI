#Def anahtarı,bir fonksiyon tanımlamak için kullanılır.
""" Aşağıda ki kod parçacığı, "selamla" adında bir 
fonksiyon tanımlar. Bu fonksiyon, "isim" adında bir
parametre alır ve verilen isme göre bir selam 
mesajı yazdırır."""
# Örnek:
def selamla(isim):
    """Verilen isme göre selam mesajı gönderir."""
    print(f"Merhaba, {isim}!")

# Fonksiyonu çağırma
selamla("Ahmet")
selamla("Mehmet")
